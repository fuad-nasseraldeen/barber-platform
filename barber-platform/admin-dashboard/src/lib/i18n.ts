/** Map backend error messages to i18n keys */
const API_ERROR_TO_KEY: Record<string, string> = {
  "This service is not available for staff": "errors.serviceNotAvailableStaff",
  "This service is not available for you": "errors.serviceBlockedForYou",
  "You are not allowed to add this service": "errors.serviceBlockedForYou",
  "A service with this name already exists": "errors.serviceNameExists",
  "Phone already in use by another customer": "errors.duplicateCustomerPhone",
  "Email already in use by another customer": "errors.duplicateCustomerEmail",
  "email must be an email": "errors.invalidEmail",
  "Invite already sent to this phone number": "staff.inviteAlreadySentPhone",
  "Invite already sent to this email": "staff.inviteAlreadySentEmail",
  "This phone is already a staff member": "staff.phoneAlreadyStaff",
  "Invite already accepted": "staff.inviteAlreadyAccepted",
  "Invite has expired": "staff.inviteExpired",
};

export function translateApiError(locale: Locale, message: string): string {
  const exact = API_ERROR_TO_KEY[message];
  if (exact) return t(locale, exact);
  if (message.startsWith("Required permission:")) {
    const lower = message.toLowerCase();
    if (lower.includes("customer")) return t(locale, "errors.customerPermissionDenied");
    if (lower.includes("service")) return t(locale, "errors.servicePermissionDenied");
    return t(locale, "errors.permissionDenied");
  }
  return message;
}
